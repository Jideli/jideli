
public class Tut25mathClassMethods {
	public static void main(String args[]) {
		System.out.println(Math.abs(-26.4)); // absolute value: take any number and give absolute value, no matter if negative
		System.out.println(Math.ceil(7.8)); // rounds up
		System.out.println(Math.floor(7.4)); // rounds down
		System.out.println(Math.max(8.6,5.2)); // give 2 numbers and it gives maxium of those two
		System.out.println(Math.min(8.6,5.2)); // take the lesser
		System.out.println(Math.pow(5,3)); // 5 to the power of 3
		System.out.println(Math.sqrt(9)); // 5 to the power of 3
	}

}
